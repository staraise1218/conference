package com.xq.conference.model;

import java.util.Date;

public class OrderSimpleInfo {
	
	private Integer ocid;//订单详情id
	private Date ocbegintime;//开始时间 --------------------------------------999
	private Date ocendtime;//结束时间
	private Date ocdockingtime;//对接时间 ------------------------------------999
	private String id;//产生订单的用户id --------------------------888
	private Integer opassstate;//审核部门是否通过(1.未通过  2.通过) ---------------999
	private String rname;//会议室名称 ----------------999
	private Double price;//订单总价格
	private String uname;//用户名
	private Integer otakeorder;
	private Integer orderstate;
	public OrderSimpleInfo(Integer ocid, Date ocbegintime, Date ocendtime, Date ocdockingtime, String id,
			Integer opassstate, String rname, Double price, String uname, Integer otakeorder, Integer orderstate) {
		super();
		this.ocid = ocid;
		this.ocbegintime = ocbegintime;
		this.ocendtime = ocendtime;
		this.ocdockingtime = ocdockingtime;
		this.id = id;
		this.opassstate = opassstate;
		this.rname = rname;
		this.price = price;
		this.uname = uname;
		this.otakeorder = otakeorder;
		this.orderstate = orderstate;
	}
	public OrderSimpleInfo() {
		super();
		// TODO Auto-generated constructor stub
	}
	public Integer getOcid() {
		return ocid;
	}
	public void setOcid(Integer ocid) {
		this.ocid = ocid;
	}
	public Date getOcbegintime() {
		return ocbegintime;
	}
	public void setOcbegintime(Date ocbegintime) {
		this.ocbegintime = ocbegintime;
	}
	public Date getOcendtime() {
		return ocendtime;
	}
	public void setOcendtime(Date ocendtime) {
		this.ocendtime = ocendtime;
	}
	public Date getOcdockingtime() {
		return ocdockingtime;
	}
	public void setOcdockingtime(Date ocdockingtime) {
		this.ocdockingtime = ocdockingtime;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Integer getOpassstate() {
		return opassstate;
	}
	public void setOpassstate(Integer opassstate) {
		this.opassstate = opassstate;
	}
	public String getRname() {
		return rname;
	}
	public void setRname(String rname) {
		this.rname = rname;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public Integer getOtakeorder() {
		return otakeorder;
	}
	public void setOtakeorder(Integer otakeorder) {
		this.otakeorder = otakeorder;
	}
	public Integer getOrderstate() {
		return orderstate;
	}
	public void setOrderstate(Integer orderstate) {
		this.orderstate = orderstate;
	}
	@Override
	public String toString() {
		return "OrderSimpleInfo [ocid=" + ocid + ", ocbegintime=" + ocbegintime + ", ocendtime=" + ocendtime
				+ ", ocdockingtime=" + ocdockingtime + ", id=" + id + ", opassstate=" + opassstate + ", rname=" + rname
				+ ", price=" + price + ", uname=" + uname + ", otakeorder=" + otakeorder + ", orderstate=" + orderstate
				+ "]";
	}
}
