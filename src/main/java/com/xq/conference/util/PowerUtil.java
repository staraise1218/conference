package com.xq.conference.util;

/**
 * 权限工具类
 * @ClassName PowerUtil
 * @Author yangweihang
 * @Date 2018年9月6日 上午9:16:06
 */
public class PowerUtil {
	
	public static final int ONE = 1;
	
	public static final int TWO = 2;
	
	public static final int THREE = 3;
	
	public static final int FOUR = 4;
	
	/**
	 * 获得权限名称
	 * yangweihang
	 * @Date 2018年9月6日 上午9:18:12
	 * @param key
	 * @return
	 */
	public static String getpowername(int key) {
		String pname = "";
		switch (key) {
		case ONE:
			pname = "普通用户";
			break;
		case TWO:
			pname = "审批部门";
			break;
		case THREE:
			pname = "服务部门";
			break;
		case FOUR:
			pname = "管理员";
			break;
		}
		return pname;
	}
}
